#include "libft.h"

void	ft_lstadd_front(t_list **lst, t_list *new)
{
	if (!lst)
		return ;
	if (!*lst)
		*lst = new;
	else if (new)
	{
		new->next = *lst;
		*lst = new;
	}
}
